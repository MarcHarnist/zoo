include_once ('model/clean-accents.php');

function cleanUrl( $url) {
	
	$url = cleanAccents($url);
	$erase = ['/', ' ', '"'];
    $url = str_replace( $erase, '-', $url );//replace keyboard spaces by -
	$url = strtolower($url);
	$url = stripslashes($url);
    return $url;
}	



